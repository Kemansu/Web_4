package ru.lab.SpringLab.dto;


public class ResultDTO {
    private double x;
    private String y;
    private double R;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }

    public double getR() {
        return R;
    }

    public void setR(double r) {
        R = r;
    }
}
